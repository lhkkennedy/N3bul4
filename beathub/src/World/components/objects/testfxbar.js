function timebites(time){
    var timebites = time.split(":")
    let hours = Number(timebites[0])
    let minutes = Number(timebites[1] + Number(hours * 60))
    let seconds = Number(timebites[2] + Number(minutes * 60))
    return seconds;
}
function loadfxtriggers(dataSelect) {
  //   const json = JSON.stringify(data);
  var fxtriggers = [];
  for (var x = 0; x < dataSelect.length; x++) {
    var seconds = timebites(dataSelect[x]["midi-item-time"])
    fxtriggers.push(seconds);
  }
  return fxtriggers;
}

